// Function to get a daily scripture based on the day of the year
export const getDailyScripture = (): Scripture => {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);

  // Use day of year to select a scripture
  const index = dayOfYear % scriptureDatabase.length;
  return scriptureDatabase[index];
};
